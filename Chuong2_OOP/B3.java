package chuong2_NgoVanHiep;
public class B3 {
	public static void main(String[] args) {
		int a = 10, b = 100;
		System.out.println("a = 10 ,b = 100");
		System.out.println("a|b = " + (a|b));
		System.out.println("a&b = " + (a&b));
		System.out.println("a^b = " + (a^b));
		System.out.println("~a = " + ~a);
		System.out.println("a>>2 = " + (a>>2));
		System.out.println("b<<2 = " + (b<<2));
	}
}